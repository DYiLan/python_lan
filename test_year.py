def year_r(year):
    if ((year % 4) == 0) and ((year % 100) != 0):
        return "闰年"
    elif(year % 400) == 0:
        return"闰年"
    else:
        return"平年"


if __name__ == "__main__":
    years = int(input("请输入年份："))  # 输入年份
    print(year_r(years))
