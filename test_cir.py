import math  # 调用数学函数包
r = float(input("请输入圆的半径："))  # 输入圆的半径　input　输入
c = 2 * math.pi * r  # 计算圆的周长
s = math.pi * (r**2)  # 计算圆的面积
print("圆的周长：", c)  # 输出圆的周长
print("圆的面积：", s)  # 输出圆的面积

